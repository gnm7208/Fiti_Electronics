// Shared utilities (pure functions - no DOM dependency, unit-testable)

/** Escape text for safe interpolation into HTML. */
export function escapeHtml(value) {
    return String(value)
        .replaceAll('&', '&amp;')
        .replaceAll('<', '&lt;')
        .replaceAll('>', '&gt;')
        .replaceAll('"', '&quot;')
        .replaceAll("'", '&#39;');
}
